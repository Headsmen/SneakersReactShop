import { z } from 'zod';

export const orderSchema = z.object({
  name: z
    .string()
    .trim()
    .min(1, 'Имя обязательно для заполнения')
    .min(2, 'Имя должно содержать минимум 2 символа')
    .max(50, 'Имя не должно превышать 50 символов')
    .regex(/^[а-яА-ЯёЁa-zA-Z\s-]+$/, 'Имя может содержать только буквы, пробелы и дефисы'),

  phone: z
    .string()
    .trim()
    .min(1, 'Номер телефона обязателен для заполнения')
    .regex(/^(\+7|8)?[\s-]?\(?[0-9]{3}\)?[\s-]?[0-9]{3}[\s-]?[0-9]{2}[\s-]?[0-9]{2}$/,
      'Введите корректный номер телефона'),

  email: z
    .string()
    .trim()
    .min(1, 'Email обязателен для заполнения')
    .email('Введите корректный email адрес'),

  city: z
    .string()
    .trim()
    .min(1, 'Город обязателен для заполнения')
    .min(2, 'Название города должно содержать минимум 2 символа')
    .max(50, 'Название города не должно превышать 50 символов')
    .regex(/^[а-яА-ЯёЁa-zA-Z\s-]+$/, 'Название города может содержать только буквы, пробелы и дефисы')
});
